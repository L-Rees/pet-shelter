package petShelter;

public class VirtualPet {
	String name;
	String description;
	int hunger;
	int thirst;
	int boredom;

	public VirtualPet(String name, String description, int hunger, int thirst, int boredom) {
		this.name = name;
		this.description = description;
		this.hunger = hunger;
		this.thirst = thirst;
		this.boredom = boredom;
		
		
	}

}
